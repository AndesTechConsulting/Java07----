package org.andestech.learning.rfb18;

public class Customer {
    int id;
    String name;
    String sname;
    Double salary;
    int age;
    int creditRating;

    public Customer(int id, String name, String sname, double salary, int age, int creditRating) {
        this.id = id;
        this.name = name;
        this.sname = sname;
        this.salary = salary;
        this.age = age;
        this.creditRating = creditRating;
    }

}
